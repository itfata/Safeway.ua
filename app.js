const contactLinks = {
  signal: "https://signal.me/#p/+380959142234",
  whatsapp: "https://wa.me/380959142234",
};

const setLanguage = (lang) => {
  const dict = translations[lang] || translations.en;
  document.documentElement.lang = lang;

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.dataset.i18n;
    if (dict[key]) {
      el.textContent = dict[key];
    }
  });

  document.querySelectorAll("[data-i18n-html]").forEach((el) => {
    const key = el.dataset.i18nHtml;
    if (dict[key]) {
      el.innerHTML = dict[key];
    }
  });

  document.querySelectorAll(".lang-toggle button").forEach((btn) => {
    btn.classList.toggle("active", btn.dataset.lang === lang);
  });

  localStorage.setItem("safeway_lang", lang);
};

const initLang = () => {
  const saved = localStorage.getItem("safeway_lang") || "en";
  setLanguage(saved);
};

const initContactModal = () => {
  const modal = document.querySelector(".modal");
  if (!modal) return;
  const openers = document.querySelectorAll("[data-open-modal]");
  const closeBtn = modal.querySelector("[data-close-modal]");

  const signalLink = modal.querySelector("[data-signal]");
  const whatsappLink = modal.querySelector("[data-whatsapp]");
  if (signalLink) signalLink.href = contactLinks.signal;
  if (whatsappLink) whatsappLink.href = contactLinks.whatsapp;

  openers.forEach((btn) =>
    btn.addEventListener("click", () => modal.classList.add("active"))
  );

  if (closeBtn) {
    closeBtn.addEventListener("click", () => modal.classList.remove("active"));
  }

  modal.addEventListener("click", (event) => {
    if (event.target === modal) modal.classList.remove("active");
  });
};

const initYear = () => {
  const yearEl = document.querySelector("[data-year]");
  if (yearEl) yearEl.textContent = new Date().getFullYear();
};


const initReveal = () => {
  const targets = [
    ".hero-grid > div",
    ".hero-meta .meta",
    ".card",
    ".list-item",
    ".price-card",
    ".banner",
    ".footer-card",
  ];

  const elements = Array.from(new Set(
    targets.flatMap((selector) => Array.from(document.querySelectorAll(selector)))
  ));

  elements.forEach((el, index) => {
    el.classList.add("reveal");
    el.style.transitionDelay = `${Math.min(index * 60, 240)}ms`;
  });

  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("is-visible");
          observer.unobserve(entry.target);
        }
      });
    },
    { threshold: 0.18 }
  );

  elements.forEach((el) => observer.observe(el));
};

const init = () => {
  initLang();
  initContactModal();
  initYear();
  initReveal();

  document.querySelectorAll(".lang-toggle button").forEach((btn) => {
    btn.addEventListener("click", () => setLanguage(btn.dataset.lang));
  });
};

document.addEventListener("DOMContentLoaded", init);
